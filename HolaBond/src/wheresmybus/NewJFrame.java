    private void jComboStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboStopActionPerformed
      
            
        try {
            // Set stop
            String stopName = (String) jComboStop.getSelectedItem();
            parser.SetStopName(stopName);
            parser.SetStopNumber();
          
            parser.read2();
                    
            // Search for the closest bus times
            parser.GetCurrentTime();
            parser.GetClosestTime();
            parser.GetNextClosestTime(); 
            
            // Print results
            PanelHora.setVisible(true);
            LabelHora.setText(parser.UpcomingBusTime());
            LabelHora.setVisible(true);
            LabelMinutos.setText(parser.NextBusDiff());
            LabelMinutos.setVisible(true);
            LabelSiguiente.setText(parser.NextBusTime());
            LabelSiguiente.setVisible(true);
            
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
            
           
    }//GEN-LAST:event_jComboStopActionPerformed

    private void jComboLineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboLineActionPerformed
            
            PanelHora.setVisible(false);
            LabelHora.setVisible(false);
            LabelMinutos.setVisible(false);
            LabelSiguiente.setVisible(false);
            //Get Line
            jComboStop.setEnabled(false);

            Object selected = jComboLine.getSelectedItem();
            if ( selected == "Linea")
            {
            jComboDirection.setEnabled(false);
            }
            else {
                try {
                    int line = Integer.valueOf((String) selected);
                    parser.SetLine(line);
                    parser.SetInputFile();
                    
                    parser.read3();
                    final DefaultComboBoxModel directions = new DefaultComboBoxModel(parser.directionList);
                    jComboDirection.setModel(directions);
                    
                    jComboDirection.setEnabled(true);
                } catch (IOException ex) {
                    Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

    }//GEN-LAST:event_jComboLineActionPerformed

