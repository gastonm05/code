    private void jComboDirectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboDirectionActionPerformed
        try {
            // Get direction
            
            String selected = (String) jComboDirection.getSelectedItem();
            if ( selected == "Direccion")
            {
            jComboStop.setEnabled(false);
            LabelMinutos.setVisible(false);
            LabelHora.setVisible(false);
            LabelSiguiente.setVisible(false);
            PanelHora.setVisible(false);
            }
            else{
            parser.SetDirectionName(selected);
            parser.SetDirectionNumber();
            parser.read();
            parser.GetStopList();
            final DefaultComboBoxModel stops = new DefaultComboBoxModel(parser.stopList);
            jComboStop.setModel(stops);
            jComboStop.setEnabled(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_jComboDirectionActionPerformed

